uint32_t Hal_Psram_IsValid(E_SpiSlave_t eSlvIdx)
{
    if (eSlvIdx >= SPI_SLAVE_MAX)
        return 0;
    
    if (g_u8aHalPsramID[eSlvIdx] == NO_PSRAM)
        return 0;
    
    return 1;
}


